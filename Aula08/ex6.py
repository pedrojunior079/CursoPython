num1 = 50
num2 = 32

if num1 == num2:
   print("num1 é igual num2")
elif num1 != num2:
   print("num1 é diferente de num2")
elif num1 > num2:
   print("num1 é maior que num2")
elif num1 < num2:
   print("num1 é menor que num2")
if num1 >= num2:
   print("num1 é maior ou igual a num2")
if num1 <= num2:
   print("num1 é menor ou igual a num2")   

